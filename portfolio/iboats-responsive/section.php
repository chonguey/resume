				<h2><i>The #1 Boating Site, Rebuilt</i></h2>
				<p>
					<img id="iboats-logo" src="./iboats-responsive/img/logo-iboats.png" />
					<a href="http://www.iboats.com/press-releases/102104.html"><img id="inc-500-logo" src="../img/inc500iboats.gif" /></a>
					With over a million pages of products, classifieds, help articles, discussion forums, and electronic catalogs, updating <a href="http://www.iboats.com">iboats.com</a> for the mobile era was no simple undertaking.
					Over a 12 year period prior, many programmers and designers had built isolated sections of iboats' web eco-system, using disparete template systems, style sheets and coding techniques.
					While functional, the entirety of iboats.com lacked visual cohesion and technical unification.<BR /> <BR />
					Using <A href="https://developers.google.com/webmasters/mobile-sites/mobile-seo/configurations/responsive-design?hl=en">Google's recommendation of responsive design</A> as my guiding principle,
					I led the re-engineering project to create a modern web eco-system that would work on any device, all without
					adversely affecting the sales or SEO rankings of the leading internet marine business.<BR /><BR />
					The end result was a cohesive, intuitive experience for all devices that increased SEO rankings, traffic and conversions across the board.
				</p>
			</section>
			<section>
				<h2><i>Key Project Highlights</i></h2>
				<ul>
					<li>All website templates converted to semantic <a href="http://www.html5rocks.com/en/">HTML5</a>, stripping layers of uneccessary complexity and code "bloat."</li>
					<li>CSS re-engineered and consolidated in a central repository of over 90 style libraries organized by function, with style guides for junior developers and marketers.</li>
					<li><A href="http://en.wikipedia.org/wiki/Progressive_enhancement/">Progressive enhancement</A> delivered a functionaly complete experience on Desktop PCs, Tablets and Smartphones, while mainting compatability with legacy browsers.</li>
					<li>100+ JavaScript libraries ported to <a href="https://jquery.com/">jQuery</a>, delivering the latest interactive features for desktops and mobile devices.</li>
					<li><A href="http://schema.org/">SEO Micro-data</a> and canonicalization were added to enhance existing SEO strategies.</li>
					<li>Code minification, distributed CDNs, and image optimization were employed to ensure the fastest page loads, even during peak traffic periods.</li>
				</ul>
			</section>
