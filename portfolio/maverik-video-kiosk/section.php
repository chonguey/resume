				<p>
					When <a href="http://www.maverik.com/">Maverik</a> wanted an interactive video marketing tool,
					they needed to go outside their usual advertising channels to find the right technological solution.
					<br /><br />
					Working in conjunction with Maverik and their marketing partner, <a href="http://catapultimpact.com/">Catapult Impact</a>,  
					I developed the interactive video kiosk system to help promote Maverik's new loyalty card programs.
					<br /><br />					
				</p>
			</section>
			<section>
				<h2><i>Key Project Highlights</i></h2>
				<ul>
					<li>The video kiosk system features a simple, minimalistic interface that allows consumers to interactively learn about customer loyalty card programs, smart phone apps, and current promotions.</li>					
					<li>The video playlist and banner ads are remotely administered using an encrypted Content Management System.</li>
					<li>New videos, ads &amp; updates are pushed out to all remote video kiosks on a synchronized schedule.</li>
					<li>Project was completed on time and on budget, with details and polish that greatly exceeded client expectations.</li>
					<li>Built solely upon Flowplayer, jQueryUI and a LAMP stack, no additional middle-ware is present to introduce unknown security risks.</li>					
				</ul>
